package com.minhld.copynsee.data;

public class Word {
	private String word;
	private String evMean;
	private String evProfMean;
	private String sameWord;
	private String eeMean;
	private String mean;
	
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	
	public String getEvMean() {
		return evMean;
	}
	public void setEvMean(String evMean) {
		this.evMean = evMean;
	}
	
	public String getEvProfMean() {
		return evProfMean;
	}
	public void setEvProfMean(String evProfMean) {
		this.evProfMean = evProfMean;
	}
	
	public String getSameWord() {
		return sameWord;
	}
	public void setSameWord(String sameWord) {
		this.sameWord = sameWord;
	}
	
	public String getEeMean() {
		return eeMean;
	}
	public void setEeMean(String eeMean) {
		this.eeMean = eeMean;
	}
	
	public String getMean() {
		return mean;
	}
	public void setMean(String mean) {
		this.mean = mean;
	}
	
}
